using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CarManufacturer
{
    public class Car
    {
        // fields
        private string make;
        private string model;
        private int year;
        private double fuelConsumption;
        private double fuelQuantity;
        private Engine engine;
        private Tire[] tires;

        // constructors
        public Car()
        {
            Make = "VW";
            Model = "Golf";
            Year = 2025;
            FuelQuantity = 200;
            FuelConsumption = 10;
        }

        public Car(string make, string model, int year) : this()
        {
            this.Make = make;
            this.Model = model;
            this.Year = year;
        }

        public Car(string make, string model, int year, double fuelQuantity, double fuelConsumption) : this(make, model, year)
        {
            FuelConsumption = fuelConsumption;
            FuelQuantity = fuelQuantity;
        }

        public Car(string make, string model, int year, double fuelQuantity, double fuelConsumption, Engine engine, Tire[] tires) : this(make, model, year, fuelConsumption, fuelQuantity)
        {
            Engine = engine;
            Tires = tires;
        }
        // properties
        public Tire[] Tires
        {
            get { return tires; }
            set { tires = value; }
        }

        public Engine Engine
        {
            get { return engine; }
            set { engine = value; }
        }

        public string Make { get { return make; } set { make = value; } }
        public string Model { get { return model; } set { model = value; } }
        public int Year { get { return year; } set { year = value; } }
        public double FuelConsumption { get { return fuelConsumption; } set { fuelConsumption = value; } }
        public double FuelQuantity { get { return fuelQuantity; } set { fuelQuantity = value; } }

        //methods
        public void Drive(double distance)
        {
            double neededFuel = distance * fuelConsumption;

            if (neededFuel < fuelQuantity)
            {
                fuelQuantity -= neededFuel;
            }
            else
            {
                Console.WriteLine("Not enough fuel to perform this trip!");
            }
        }

        public string WhoAmI()
        {
            StringBuilder result = new StringBuilder();

            result.AppendLine($"Make: {this.Make}");
            result.AppendLine($"Model: { this.Model}");
            result.AppendLine($"Year: { this.Year}");
            result.AppendLine($"Fuel: { this.FuelQuantity:F2}");

            return result.ToString().Trim();
        }
    }
}
